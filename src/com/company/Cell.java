package com.company;

public class Cell {

    public int xMin;
    public int xMax;
    public int yMin;
    public int yMax;
    public int zMin;
    public int zMax;

    public Cell( int newxMin, int newxMax, int newyMin, int newyMax, int newzMin, int newzMax){
        xMin = newxMin;
        xMax = newxMax;
        yMin = newyMin;
        yMax = newyMax;
        zMin = newzMin;
        zMax = newzMax;
    }




}
